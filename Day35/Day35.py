# What is an API key?
# When you use a service (weather data, SMS, payments, etc.), the provider needs 
# to verify who is making the request.
# Hence a secret token is given called API Key
# If that key leaks, someone else can use the service in your name


# Environment Variables
# Instead of storing secrets inside code, you store them in your system.
# When the program runs, it fetches the value from there.

# Authentication vs Authorization
# In simple terms:
# Authentication → who you are
# Authorization → what you are allowed to do