# "Introduction to Web Developent with Flask"

# What is Flask?
# Flask is a lightweight web framework for Python.

# It allows you to:
# - Build websites
# - Create APIs
# - Handle HTTP requests
# - Serve HTML pages


# What is a Route?
# A route defines what happens when a user visits a URL.
# Ex:
# @app.route("/")
# Means:
# When someone visits the homepage → run this function


# Dynamic Routes
# Flask can accept variables in URLs.
# Example:
# @app.route("/username/<name>")
# def greet(name):
#     return f"Hello {name}"



# Why Flask Is Important
# - Flask is used for:
# - Backend development
# - REST APIs
# - Machine learning dashboards
# - Data science apps