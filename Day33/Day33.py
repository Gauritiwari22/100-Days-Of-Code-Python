# SMTP (Simple Mail Transfer Protocol)
# SMTP is the standard protocol used to send emails over the internet.

# Working:
# - Client connects to SMTP server
# - Server verifies login credentials
# - Message is transmitted
# - Server delivers mail to recipient
# - Python provides the smtplib module to implement this process.

