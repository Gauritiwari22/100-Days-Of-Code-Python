# "Day 36"

# Automation Thinking
# Until now, programs were mostly:
# run ->  give output -> stop
# Now we move to:
# program watches something -> if condition happens -> action taken


# Event-Driven Programming
# An event is something that triggers behaviour.
# Examples:
# value crosses limit
# new email arrives
# button click
# timer hits
# Program stays quiet until event occurs.



# Services Talking to Each Other
# Instead of one program doing everything:

# Service A gives data  
# Service B gives extra info  
# Service C communicates to user
# Your job = glue them together.

# This is how modern apps are built.



# APIs give ugly, heavy, technical responses.

# Your responsibility:
# extract
# simplify
# present clearly

# The value of a programmer is not fetching data,
# it’s making it understandable.

# Protecting Secrets

# Never expose:
# API keys
# passwords

# tokens
# Because code might go to GitHub.
# Hence -> environment variables.
# Professional habit.



# Reusability Mindset

# Instead of hardcoding everything, design so that:
# stock name can change
# percentage can change
# company can change
# Good developers think in reusable systems.