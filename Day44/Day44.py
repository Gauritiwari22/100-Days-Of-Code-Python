# Element Selector
# Targets all elements of that type.

# h1 {
#   color: red;
# }


# Class Selector
# HTML:
# <p class="intro">Hello</p>
# CSS:
# .intro {
#   color: blue;
# }
# The . indicates a class.
# Multiple elements can share the same class.
    

# ID Selector
# HTML:
# <h1 id="main-title">My Site</h1>
# CSS:
# #main-title {
#   color: green;
# }
# The # indicates an ID.
# IDs should be unique per page.


# Descendant Selector
# Targets elements inside another element.
# div p {
#   color: purple;
# }
# Meaning:
# All <p> elements inside a <div> become purple.


# Multiple Selector
# Apply the same style to multiple elements.

# h1, h2, h3 {
#   color: teal;
# }


# Pseudo-Classes
# Pseudo-classes style elements based on state.
# Example:
# a:hover {
#   color: red;
# }

# When the mouse hovers over a link = text becomes red.


