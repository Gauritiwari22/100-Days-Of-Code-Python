# "Day 43"
# What is CSS?

# CSS = Cascading Style Sheets
# It controls styling (Colors, Fonts, Spacing, Layout, Size, Alignment)

# How to Add CSS (3 Ways)
# 1. Inline CSS
# <p style="color: red;">Hello</p>

# 2. Internal CSS
# <style>
#   body {
#     background-color: lightblue;
#   }
# </style>

# 3. External CSS
# <link rel="stylesheet" href="styles.css">

# Inside styles.css:

# body {
#   background-color: lightblue;
# }

# Common CSS Properties 
# Background Color
# body {
#   background-color: #EAF6F6;
# }
# Text Color
# h1 {
#   color: #66BFBF;
# }
# Font Size
# p {
#   font-size: 20px;
# }
# Border
# img {
#   border-radius: 50%;
# }

# Class Selector
# <p class="intro">Hello</p>

# .intro {
#   color: red;
# }

# . means class.

# ID Selector
# <h1 id="main-heading">Hi</h1>
# #main-heading {
#   color: green;
# }

# # means ID.

# IDs must be unique.
