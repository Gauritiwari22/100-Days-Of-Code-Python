# # What is HTTP?

# # HTTP = HyperText Transfer Protocol
# # It is the protocol used to transfer data over the web.
# # It works on:
# # Client → sends request
# # Server → sends response

# # HTTP Methods (Very Important)
# # POST → Create data
# # PUT → Update data
# # DELETE → Delete data
# # GET → Retrieve data

# What is an Endpoint?
